package basicjava;

public class Double {

	public static void main(String[] args) {
 
		// Program to show dynamic variable initialization of variable
		
		double a = 3.0, b = 4.0;

		// c is dynamically initialized
		double c = Math.sqrt(a * a + b * b);

		System.out.println("D1 is " + c);

	}

}
